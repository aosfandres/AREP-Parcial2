package edu.escuelaing.arep;

public class Lambda {
    
    /**
     * metodo para calcular tangente
     * @param n
     * @return
     */
    public Double tan(Double n){
        return Math.tan(n);
    }

    /**
     * metodo para calcular arcoseno 
     * @param n
     * @return
     */
    public Double asin(Double n){
        return Math.asin(n);
    }
}
